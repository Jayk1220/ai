--[VI] 인덱스: SELECT 속도를 높이기 위해
DROP TABLE EMP01;
CREATE TABLE EMP01 AS SELECT * FROM EMP; --EMP 테이블과 내용이 동일한 EMP01생성
SELECT * FROM EMP01;  --14행
INSERT INTO EMP01 SELECT * FROM EMP01; -- 데이터 뻥튀기 1번 (29), 2번(56)
SELECT COUNT(*) FROM EMP01; --EMP01 행 수
INSERT INTO EMP01 SELECT * FROM EMP01; --11번 실행
SELECT * FROM EMP01;  --11만 4천 여 행
INSERT INTO EMP01 (EMPNO, ENAME, DEPTNO) VALUES (1111, 'HONG', 50);
INSERT INTO EMP01 SELECT * FROM EMP01; --1번 (22만), 2번(45만), 3번(90만)
SELECT COUNT(*) FROM EMP01; --91만 행
SELECT * FROM EMP01 WHERE ENAME = 'HONG'; --인덱스 생성 전 조회시간 : 0.014초
--인덱스 생성
CREATE INDEX IDX_EMP01_ENAME ON EMP01(ENAME); --0.194초 (인덱스 생성)
SELECT * FROM EMP01 WHERE ENAME = 'HONG'; 0.005초
COMMIT;

INSERT INTO EMP01 SELECT * FROM EMP01; --91만->182만
SELECT COUNT(*) FROM EMP01;
ROLLBACK; --7.481초

--인덱스 삭제
DROP INDEX IDX_EMP01_ENAME;
SELECT * FROM EMP01 WHERE ENAME = 'HONG'; -- 0.015초
INSERT INTO EMP01 SELECT * FROM EMP01; --91만->182만 (1.719초)
ROLLBACK; --0.056초
DROP TABLE EMP01; -- 테이블 삭제시 해당 테이블의 인덱스도 삭제